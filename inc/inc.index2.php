                <p>Missionaries of The Church of Jesus Christ of Latter-day Saints (LDS Church)—widely known as Mormon missionaries—are volunteer representatives of the LDS Church who engage variously in proselytizing, church service, humanitarian aid, and community service.</p>
                <p>All full-time missionaries wear a name tag that gives their surname with the appropriate title ("Elder" or "Sister" in English-speaking areas, or their equivalent titles in other languages). </p>
